add_rules("mode.debug", "mode.release")
add_rules("plugin.compile_commands.autoupdate", { outputdir = ".vscode" })
set_defaultmode("debug")
-- set_policy("build.c++.modules.non_cascading_changes", true)
-- set_policy("build.c++.modules.reuse.strict", true)
-- set_policy("build.progress_style", "multirow")

add_cxxflags("-fno-rtti", { tools = { "clang", "gcc" }})
add_cxxflags("/GR-", { tools = { "clang_cl", "cl" }})
set_languages("c++latest")
set_exceptions("no-cxx")
set_warnings("allextra")
set_rundir("$(projectdir)")

target("carven-modules")
    set_default(false)
    set_kind("moduleonly")
    add_files("src/**.cppm")

target("carven")
    add_deps("carven-modules")
    add_files("src/carven.cpp")
    add_installfiles("(xmake/rules/*.lua)", {prefixdir = "share/carven"})

target("carven-unit-test")
    set_default(false)
    add_deps("carven-modules")
    add_files("tests/test_*.cpp")
    remove_files("tests/test_e2e.cpp")

target("carven-e2e-test")
    set_default(false)
    add_deps("carven", {inherit = false})
    add_deps("carven-modules")
    after_load(function (target)
        local carven = target:dep("carven")
        local e2e_home = path.join(os.tmpdir(), "carven-e2e-home")
        if carven then
            local carven_file = path.absolute(carven:targetfile(), os.projectdir())
            target:add("defines", "CARVEN_E2E_TOOL_PATH=\"" .. carven_file .. "\"")
        end
        target:set("runenv", "HOME", e2e_home)
        target:set("runenv", "LOCALAPPDATA", e2e_home)
        target:set("runenv", "XDG_CACHE_HOME", e2e_home)
    end)
    add_files("tests/test_main.cpp")
    add_files("tests/test_e2e.cpp")
